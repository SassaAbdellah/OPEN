package de.avci.openride.tools.imagecreation.constants;

import java.awt.Color;


/** Defines some colors for use with OpenRide
 * 
 * @author jochen
 *
 */
public class OpenRideColors {
	
	
	
	/** Background Color "OpenRideGreen" was found to be
	 *  "99be19" in html  notation
	 *  which is 153 190 25 in RGB
	 */
	public static final Color openrideBackgroundGreen=new Color(153,190,25);
	
	
	/** Highlight  color for OpenRidegreen backgrounds
	 *  "aecc48" in html notation
	 *  174,204,72 in RGB
	 *  
	 */
	public static final	Color openrideGradientHighlightGreen=new Color(174,204,72);;
	
	/** Color for frame to be drawn around generic button.
	 * 
	 */
	public static final Color openrideFrameGreen=openrideGradientHighlightGreen;
	

}
